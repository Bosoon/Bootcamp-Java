public class WholeNumbers {
    public static void main(String[] args) {
        int numberOfFans = 5000;

        System.out.println("There are " + numberOfFans + " fans at the Quidditch games.");

        long globalPopulations = 78000000000L;
        System.out.println("There are " + globalPopulations + " people on earth.");

        System.out.println("10 is an even number, since dividing 10 by 2 has a reminder of: " + ( 10 % 2));
        System.out.println("5 is an odd number, since dividing 5 by 2 has a reminder of: " + ( 5 % 2));

        int counter = 0;
        counter++;
        counter++;
        counter++;
        System.out.println("I count: " + counter);

        counter--;
        counter--;
        counter--;
        System.out.println("I count: " + counter);
    }
    
}

